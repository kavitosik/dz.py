num = int(input("Введите число (>999): "))

while True:
    if num > 999:
        print(f"Число сотен в вашем числе ---> {num//100}")
        print(f"Число тысяч в вашем числе ---> {num//1000}")
        break
    else:
        print("Ты что не понял??? Число должно быть больше 999 >:(")
        num = int(input("Введите число (>999): "))
