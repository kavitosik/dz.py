num = int(input("Введите любое двузначное число: "))

print(f"Число десятков в вашем числе: {num // 10}")
print(f"Число чисел в вашем числе: {num % 10}")
print(f"Сумма цифр вашего числа: {int(str(num)[0]) + int(str(num)[1])}")
print(f"Произведение цифр вашего числа: {int(str(num)[0]) * int(str(num)[1])}")
